package modificators;

public class MyMath {
	
	public final static int MY_A = 20;
	public final static int MY_B = 30;
	
	public static int sum(){
		return MY_A+MY_B;
	}
	public static int substraction(){
		return MY_A-MY_B;
	}
	public static int multiplication(){
		return MY_A*MY_B;
	}
	public static int division(){
		return MY_A/MY_B;
	}
	

}
